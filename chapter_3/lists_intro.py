# lists store ordered values within a variable demarcated by name = []

letters = ['a','b','c']

#when printing a list, it will return the square bracket format

print(letters)

#to select a particular value, we need to include the position in the list like name[0]

print(letters[0])

#these values can be manipulated by methods/functions like any data point  

print(letters[0].upper())

#REMEMBER, index position starts at ZERO not ONE 

#To access last member of a list, use -1 in index selection

print(letters[-1].upper())